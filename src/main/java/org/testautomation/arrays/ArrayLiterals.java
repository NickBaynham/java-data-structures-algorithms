package org.testautomation.arrays;

public class ArrayLiterals {
    public static void main(String[] args) {
        int[] array = {1, 2, 3, 4};
        for (int i = 0; i < array.length; i++) {
            System.out.println("Array element index " + i + " has value " + array[i]);
        }
    }
}
